class Escape
{
	public static void main(String args[])
	{
		
		// Add a tab between Ravula and Govardhan
		System.out.println("Ravula\tGovardhan");
		
		// Add a new line after Ravula
		System.out.println("Ravula\nGovardhan");
		
		// Add a carriage return after Ravula
		System.out.println("Ravula\rGovardhan");
		
		// Add a single quote between Ravula and Govardhan
		System.out.println("Ravula\'Govardhan");
		
		// Add a double quote between Ravula and Govardhan
		System.out.println("Ravula\"Govardhan");
		
		// Add a backslash between Ravula and Govardhan
		System.out.println("Ravula\\Govardhan");
	}
}